//WAP to perform arithmetic operator on two integer numbers
class arithop
{
    public static void main(String[] args) {
        int a=40;
        int b=30;
        System.out.println("value of a = "+a);
        System.out.println("value of b = "+b);
        System.out.println("value of a + b = "+(a+b));
        System.out.println("value of a-b = "+(a-b));
        System.out.println("value of a*b =" +(a*b));
        System.out.println("value of a/b = "+(a/b));
        System.out.println("value of a%b = "+(a%b));
    }
}